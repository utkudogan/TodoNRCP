    public void setNewTask(boolean newTask) {
        this.isNewTask = newTask;
        remove.setEnabled(!newTask);
        if (isNewTask()) {
            setMessage("Fill in the new task information", false);
        } else {
            setMessage("Changing task information", false);
        }
    }

    public boolean isNewTask() {
        return isNewTask;
    }

    public void setMessage(String msg, boolean isError) {
        message.setText(msg);
        if (isError) {
            message.setForeground(Color.RED);
        } else {
            message.setForeground(Color.DARK_GRAY);
        }
    }

    public void setTask(Task task) {
        this.task = task;
        description.setText(task.getDescription());
        priority.setValue(task.getPriority());
        completed.setSelected(task.isCompleted());
        dueDate.setDate(task.getDueDate());
        alert.setSelected(task.getAlert());
        daysBefore.setEnabled(task.getAlert());
        alertLabel.setEnabled(task.getAlert());
        daysBefore.setValue(task.getDaysBefore());
        obs.setText(task.getObs());
    }

    public Task getTask() {
        task.setDescription(description.getText());
        task.setPriority(((Number) priority.getValue()).intValue());
        task.setCompleted(completed.isSelected());
        task.setDueDate(dueDate.getDate());
        task.setAlert(alert.isSelected());
        task.setDaysBefore(((Number) daysBefore.getValue()).intValue());
        task.setObs(obs.getText());
        return task;
    }

