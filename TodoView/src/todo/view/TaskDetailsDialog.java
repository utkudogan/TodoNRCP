    private class FormListener implements java.awt.event.ActionListener, javax.swing.event.ChangeListener {
        FormListener() {}
        public void actionPerformed(java.awt.event.ActionEvent evt) {
            if (evt.getSource() == alert) {
                TaskDetailsDialog.this.alertActionPerformed(evt);
            }
            else if (evt.getSource() == remove) {
                TaskDetailsDialog.this.removeActionPerformed(evt);
            }
            else if (evt.getSource() == cancel) {
                TaskDetailsDialog.this.cancelActionPerformed(evt);
            }
            else if (evt.getSource() == save) {
                TaskDetailsDialog.this.saveActionPerformed(evt);
            }
        }

        public void stateChanged(javax.swing.event.ChangeEvent evt) {
            if (evt.getSource() == priority) {
                TaskDetailsDialog.this.priorityStateChanged(evt);
            }
            else if (evt.getSource() == daysBefore) {
                TaskDetailsDialog.this.daysBeforeStateChanged(evt);
            }
        }
    }// </editor-fold>//GEN-END:initComponents

    private void cancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelActionPerformed
        dispose();
    }//GEN-LAST:event_cancelActionPerformed

    private void alertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alertActionPerformed
        daysBefore.setEnabled(alert.isSelected());
        alertLabel.setEnabled(alert.isSelected());
    }//GEN-LAST:event_alertActionPerformed

    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        taskManager.removeTask(getTask().getId());
    }//GEN-LAST:event_removeActionPerformed

    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try {
            if (isNewTask()) {
                taskManager.addTask(getTask());
            } else {
                taskManager.updateTask(getTask());
            }
        } catch (ValidationException ex) {
            Exceptions.printStackTrace(ex);
        }
        cancel.doClick();
    }//GEN-LAST:event_saveActionPerformed

    private void priorityStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_priorityStateChanged
        priority.removeChangeListener(formListener);
        int value = (int) priority.getValue();
        if (value < 0) {
            priority.setValue(0);
        }
        if (value > 100) {
            priority.setValue(100);
        }
        priority.addChangeListener(formListener);
    }//GEN-LAST:event_priorityStateChanged

    private void daysBeforeStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_daysBeforeStateChanged
        daysBefore.removeChangeListener(formListener);
        int value = (int) daysBefore.getValue();
        if (value < 0) {
            daysBefore.setValue(0);
        }
        if (value > 100) {
            daysBefore.setValue(100);   // arbitrary upper bound
        }
        daysBefore.addChangeListener(formListener);
    }//GEN-LAST:event_daysBeforeStateChanged

